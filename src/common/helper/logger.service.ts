import moment from 'moment'
import chalk from 'chalk'
import shellService from './shellservice/shell.service'
import { ISO_DATE_HH, MObject } from './constants'

class LoggerService {
  setConsoleLog() {
    console.warn = this.warn.bind(this)
    console.error = this.error.bind(this)
    console.info = this.ok.bind(this)
    console.trace = this.line.bind(this)
    console.table = this.table.bind(this)
  }

  private error(...data: any[]) {
    const prefix = [
      chalk.redBright(this.timestamp(), chalk.underline(`ERROR`)),
    ].join('')
    const suffix = chalk.reset(` `)
    console.log(prefix, ...data, suffix)
  }

  private warn(...data: any[]) {
    const prefix = [
      chalk.yellowBright(this.timestamp(), chalk.underline(`WARN`)),
    ].join('')
    const suffix = chalk.reset(` `)
    console.log(prefix, ...data, suffix)
  }

  private ok(...data: any[]) {
    const prefix = [
      chalk.greenBright(this.timestamp(), chalk.underline(`OK`)),
    ].join('')
    const suffix = chalk.reset(` `)
    console.log(prefix, ...data, suffix)
  }

  private line(str = '', level = 0) {
    const [p, m] =
      level > 0 ? ['>>', ' START'] : level < 0 ? ['<<', ' END'] : ['', '']
    level = Math.abs(level)
    const funcs = [
      chalk.yellowBright,
      chalk.cyanBright,
      chalk.magentaBright,
      // chalk.blueBright,
    ]
    const fun = level === 0 ? chalk.reset : funcs[(level - 1) % funcs.length]
    const offset = Math.abs(40 - level * 8)
    const prefix = fun(p + '='.repeat(offset) + m)
    const suffix = chalk.reset(` `)
    console.log(prefix, str, suffix)
  }

  private table(log: MObject) {
    const arr = shellService.objectToString(log, true) as string[][]
    arr.map(([k, v]) => console.log(chalk.gray(k), v))
  }

  private timestamp() {
    return '[' + moment.utc().format(ISO_DATE_HH) + ']'
  }
}

export default new LoggerService()

